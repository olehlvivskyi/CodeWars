namespace CodeWars.Solution._8kyu;

// Refactor Variable Names to Variable Keywords
// https://www.codewars.com/kata/670d95c03ad7c741c1099b83
public class RefactorVariableNamesToVariableKeywords
{
    public class VariableName
    {
        public bool    @bool    = false;
        public char    @char    = '\0';
        public sbyte   @sbyte   = 0;
        public byte    @byte    = 0;
        public short   @short   = 0;
        public ushort  @ushort  = 0;
        public int     @int     = 0;
        public uint    @uint    = 0;
        public long    @long    = 0;
        public ulong   @ulong   = 0;
        public float   @float   = 0;
        public double  @double  = 0;
        public decimal @decimal = 0;
    }
}